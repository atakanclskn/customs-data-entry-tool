export const DocumentTypeBadge: React.FC<{ type: DocumentType | undefined }> = ({ type }) => {
    if (!type) return null;

    let text = 'Bilinmeyen';
    let colorClasses = 'bg-gray-600 text-white dark:bg-gray-700 dark:text-gray-200';

    if (type === DocumentType.DECLARATION) {
        text = 'Beyanname';
        colorClasses = 'bg-sky-600 text-white dark:bg-sky-900/50 dark:text-sky-300';
    } else if (type === DocumentType.FREIGHT) {
        text = 'Navlun';
        colorClasses = 'bg-teal-600 text-white dark:bg-teal-900/50 dark:text-teal-300';
    }

    return (
        <span className={`inline-block px-2 py-0.5 text-xs font-semibold rounded-full ${colorClasses}`}>
            {text}
        </span>
    );
};

